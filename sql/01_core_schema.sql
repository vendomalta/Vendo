    event_type TEXT NOT NULL,
    severity TEXT DEFAULT 'info' CHECK (severity IN ('info', 'warning', 'critical')),
    details JSONB DEFAULT '{}',
    ip_address VARCHAR(45),
    user_agent TEXT,
    browser TEXT,
    os TEXT,
    device_type TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS public.user_sessions (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
    session_token VARCHAR(255) NOT NULL,
    device_info JSONB NOT NULL DEFAULT '{}'::jsonb,
    ip_address VARCHAR(45),
    user_agent TEXT,
    last_activity TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- SECURITY VIEWS
CREATE OR REPLACE VIEW public.failed_login_attempts AS
SELECT user_id, DATE_TRUNC('hour', created_at) AS hour, COUNT(*) AS attempt_count, ARRAY_AGG(DISTINCT ip_address) AS ip_addresses
FROM public.security_logs WHERE event_type = 'login_failed' AND created_at > NOW() - INTERVAL '24 hours'
GROUP BY user_id, DATE_TRUNC('hour', created_at) HAVING COUNT(*) > 5;

CREATE OR REPLACE VIEW public.suspicious_activities AS
SELECT user_id, event_type, COUNT(*) AS occurrence_count, ARRAY_AGG(DISTINCT ip_address) AS ip_addresses, MAX(created_at) AS last_occurred, MIN(created_at) AS first_occurred
FROM public.security_logs WHERE severity = 'critical' AND created_at > NOW() - INTERVAL '7 days'
GROUP BY user_id, event_type;

